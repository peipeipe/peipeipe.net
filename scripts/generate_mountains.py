def main():
    output_path = os.path.join(os.path.dirname(__file__), "..", "_data", "mountains.json")
    seen = {}
    for row in MOUNTAINS_RAW:
        name, lat, lng, elev, tags = row
        if name not in seen:
            seen[name] = {"name": name, "lat": lat, "lng": lng, "elevation": elev, "tags": list(tags)}
        else:
            existing = set(seen[name]["tags"])
            existing.update(tags)
            seen[name]["tags"] = sorted(existing)
    result = sorted(seen.values(), key=lambda x: -x["elevation"])
    from collections import Counter
    tag_counts = Counter(t for m in result for t in m["tags"])
    print(f"合計 {len(result)} 座")
    for tag, count in tag_counts.most_common():
        print(f"  {tag}: {count}座")
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(result, f, ensure_ascii=False, indent=2)
    print(f"出力: {output_path}")

if __name__ == "__main__":
    main()
